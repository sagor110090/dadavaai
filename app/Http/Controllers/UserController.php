    public function dashboard()
    {
        // Get all client
        $clients = Client::all()->sortByDesc('id');

        // Get order of latest one month
        $orders = Order::whereMonth(
            'created_at', '=', Carbon::now()->month
        )->whereYear(
            'created_at', '=', Carbon::now()->year
        )->count();

        // Get all subscription
        $subscription = Subscription::all()->sortByDesc('id');

        return view('admin.index', compact('clients', 'subscription', 'orders'));
